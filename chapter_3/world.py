places = ['hawaii', 'alaska', 'russia', 'tokyo', 'brazil']
print("Original List")
print(places)

print("\n Sorted List")
print(sorted(places))

print("\n Original List still intact")
print(places)

places.reverse()
print("\nList in Reverse Order")
print(places)

places.reverse()
print("\nList in Original Order")
print(places)

places.sort()
print("\nList in Sorted Order")
print(places)

places.sort(reverse = True)
print("\nList in Reverse Sorted Order")
print(places)

